int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	r;

	r = 0;
	if (n == 0)
		return (0);
	while (s1[r] == s2[r] && (s1[r] != '\0' || s2[r] != '\0') && (r <= n - 1))
	{
		while (s1[r] == s2[r] && (r != n - 1))
		{
			r++;
		}
		if (s1[r] != s2[r])
		{
			return (s1[r] - s2[r]);
		}
	}
}
