int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	r;
	int				s;
	int				c;

	r = 0;
	s = 0;
	c = 0;
	while ((s1[r] != '\0' && s2[s] != '\0') && (c == 0) && (r != n))
	{
		if (s1[r] == s2[s])
		{
			c += 0;
			r++;
			s++;
		}
		else if (s1[r] < s2[r])
			c -= 1;
		else if (s1[r] > s2[r])
			c += 1;
	}
	return (c);
}
