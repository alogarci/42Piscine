int	ft_str_is_alpha(char *str)
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (str[n] != '\0')
	{
		if ((str[n] < 'A' || str[n] > 'Z') && (str[n] < 'a' || str[n] > 'z'))
		{
			i++;
		}
		n++;
	}
	if (i > 0)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
