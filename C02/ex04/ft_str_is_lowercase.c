int	ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	while (*str != '\0')
	{
		if (*str < 'a' || *str > 'z')
		{
			i++;
		}
		str++;
	}
	if (i > 0)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
