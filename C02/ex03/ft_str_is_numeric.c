int	ft_str_is_numeric(char *str)
{
	int i;

	i = 0;
	while (*str != '\0')
	{
		if (*str < '0' || *str > '9')
		{
			i++;
		}
		*str++;
	}
	if (i > 0)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
