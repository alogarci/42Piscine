int	ft_str_is_numeric(char *str)
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (str[n] != '\0')
	{
		if (str[n] < '0' || str[n] > '9')
		{
			i++;
		}
		n++;
	}
	if (i > 0)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
