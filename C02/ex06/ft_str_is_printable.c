int	ft_str_is_printable(char *str)
{
	int i;

	i = 0;
	while (*str != '\0')
	{
		if (*str < 32 || *str > 126)
		{
			i++;
		}
		*str++;
	}
	if (i > 0)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}