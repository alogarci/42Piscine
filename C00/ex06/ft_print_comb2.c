#include <unistd.h>

void	ft_putchar(char x)
{
	write(1, &x, 2);
}

void	ft_print_comb2(void)
{
	int n[4];

	n[0] = '0';
	n[1] = '0';
	n[2] = '0';
	n[3] = '0';

	ft_putchar(n[0]);
}
